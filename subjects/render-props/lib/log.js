export function listen(cb) {
  var lines = script.split('\n').reduce((lines, line) => {
    if (line !== '')
      lines.push(line)

    return lines
  }, [])

  function notify() {
    if (lines.length) {
      cb([ lines.shift() ])
      setTimeout(notify, Math.random() * 1500)
    }
  }

  notify()
}
